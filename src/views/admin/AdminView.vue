    data() {
        return {
            data_user: {},
            user: {},
            name: '',
            email: '',
            // password: '',
            level: '',
            id: ''
        }
    },
    created() {
        this.datauser()
    },
    mounted() {
        this.data_edit()
    },
    methods: {
        datauser() {
            axios.get('http://localhost:8000/api/getuser')
                .then(({ data }) => {
                    console.log(data)
                    this.data_user = data
                })
                .catch(
                    (err) => {
                        console.log(err)
                    }
                )
        },
        save() {
            axios.post('http://localhost:8000/api/createuser', this.user)
                .then(
                    ({ data }) => {
                        console.log(data)
                        swal({
                            title: 'Sukses tambah user',
                            icon: 'success',
                        })
                        setTimeout(() => {
                            location.reload()
                        }, 1200);
                    }
                )
        },
        hapus(s) {
            swal({
                title: 'Ingin menghapus data user ini?',
                icon: 'warning',
                buttons: true,
                dangerMode: true
            }).then((hapus) => {
                if (hapus) {
                    axios.delete('http://localhost:8000/api/deleteuser/' + s.id)
                    swal({ icon: 'success', title: 'Sukses delete user' })
                    setTimeout(() => {
                        location.reload()
                    }, 1200);
                }
            })
        },
        data_edit(s) {
            axios.get('http://localhost:8000/api/getuser/' + s.id)
                .then(
                    (response) => {
                        console.log(response.data[0])
                        this.id = response.data[0].id
                        this.name = response.data[0].name
                        this.email = response.data[0].email
                        this.level = response.data[0].level
                        // this.name = response.data[0].name
                    }
                )
        },
        edit() {
            let data = {
                id: this.id,
                name: this.name,
                email: this.email,
                level: this.level,
            }
            axios.put('http://localhost:8000/api/updateuser/' + this.id, data)
                .then(
                    (data) => {
                        console.log(data)
                        swal({
                            icon: 'success',
                            title: 'Sukses update data',
                        })
                        setTimeout(() => {
                            location.reload()
                        }, 1200);
                    }
                )
        }
    }
